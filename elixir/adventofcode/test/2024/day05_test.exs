defmodule Advendofcode.Solutions.Y24.Day05Test do
  alias AoC.Input, warn: false
  alias Advendofcode.Solutions.Y24.Day05, as: Solution, warn: false
  use ExUnit.Case, async: true

  # To run the test, run one of the following commands:
  #
  #     mix AoC.test --year 2024 --day 5
  #
  #     mix test test/2024/day05_test.exs
  #
  # To run the solution
  #
  #     mix AoC.run --year 2024 --day 5 --part 1
  #
  # Use sample input file:
  #
  #     # returns {:ok, "priv/input/2024/day-05-mysuffix.inp"}
  #     {:ok, path} = Input.resolve(2024, 5, "mysuffix")
  #
  # Good luck!

  defp solve(input, part) do
    problem =
      input
      |> Input.as_file()
      |> Solution.parse(part)

    apply(Solution, part, [problem])
  end

  test "part one example" do
    input = ~S"""
    47|53
    97|13
    97|61
    97|47
    75|29
    61|13
    75|53
    29|13
    97|29
    53|29
    61|53
    97|53
    61|29
    47|13
    75|47
    97|75
    47|61
    75|61
    47|29
    75|13
    53|13

    75,47,61,53,29
    97,61,53,29,13
    75,29,13
    75,97,47,61,53
    61,13,29
    97,13,75,29,47
    """

    assert 143 == solve(input, :part_one)
  end

